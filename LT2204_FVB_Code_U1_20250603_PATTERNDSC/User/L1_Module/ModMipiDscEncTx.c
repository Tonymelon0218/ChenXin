void Mod_MipiTxDscEnc_PpsParaInit()
{
    memset(&g_stMipiTxDscEncConfig, 0, sizeof(DSCConfig_Stru));
    g_stMipiTxDscEncConfig.ucSliceNum = 8;  // 2D->4; 3D->8
    g_stMipiTxDscEncConfig.ucDscNumber = 8; // 2D->4; 3D->8
    g_stMipiTxDscEncConfig.ucExtraHact = 0;
    g_stMipiTxDscEncConfig.ucColorDepth = BPC8;
    g_stMipiTxDscEncConfig.ucCompressRatio = 3;
    g_stMipiTxDscEncConfig.ucOutBpp = 8;
    g_stMipiTxDscEncConfig.usSliceHeight = ((u16)ucuzEncPPS[10] << 8) + ucuzEncPPS[11];
    g_stMipiTxDscEncConfig.usSliceWidth = ((u16)ucuzEncPPS[12] << 8) + ucuzEncPPS[13];
    g_stMipiTxDscEncConfig.usChunkSize = ((u16)ucuzEncPPS[14] << 8) + ucuzEncPPS[15];
    LTLog(LOG_DEBUG, "DscEnc Slice = %bd,CompressRatio = %bd", g_stMipiTxDscEncConfig.ucSliceNum, g_stMipiTxDscEncConfig.ucCompressRatio);
    LTLog(LOG_DEBUG, "DscEnc SliceHeight = %d,SliceWidth = %d,ChunkSize = %d", g_stMipiTxDscEncConfig.usSliceHeight, g_stMipiTxDscEncConfig.usSliceWidth, g_stMipiTxDscEncConfig.usChunkSize);
}

void Mod_MipiTxDscEnc_Clk_Chk(void)
{
    u32 TempClock;

    Drv_System_FmSet(TOP_FM, 0x0F);
    Ocm_Delay1ms(5);
    TempClock = Drv_System_FmGet(TOP_FM, 0);
    LTLog(LOG_INFO, "Ad_csctx_half_pix_clk = %lukhz", TempClock);

    Drv_System_FmSet(TOP_FM, 0x12);
    Ocm_Delay1ms(5);
    TempClock = Drv_System_FmGet(TOP_FM, 0);
    LTLog(LOG_INFO, "Ad_dsctx_dsc_rd_clk = %lukhz", TempClock);

    Drv_System_FmSet(TOP_FM, 0x13);
    Ocm_Delay1ms(50);
    TempClock = Drv_System_FmGet(TOP_FM, 0);
    LTLog(LOG_INFO, "Ad_dsctx_dsc_clk = %lukhz", TempClock);

    Drv_System_FmSet(TOP_FM, 0x14);
    Ocm_Delay1ms(5);
    TempClock = Drv_System_FmGet(TOP_FM, 0);
    LTLog(LOG_INFO, "Ad_dsctx_dsc_out_clk = %lukhz", TempClock);
}

void Mod_MipiTxDscEnc_ModuleEnable(void)
{
    Drv_MipiTxDscEnc_DataMux();
#if (MIPITX_VID_PATTERN_MODE != NO_PATTERN)
    Drv_MipiTxDscEnc_ClkMuxForPattern();
#else
    Drv_MipiTxDscEnc_ClkMux();
#endif
    Drv_MipiTxDscEnc_RxParamSet();
    Drv_MipiTxDscEnc_ExtraHactSet();
    Drv_MipiTxDscEnc_TimingSet();
    Drv_MipiTxDscEnc_MuxWordDly();
    Drv_MipiTxDscEnc_PpsSet(ucuzEncPPS);
    Drv_MipiTxDscEnc_ColorSpaceSet();
    Drv_MipiTxDscEnc_PpsDynamicSet();
    Drv_MipiTxDscEnc_ParamSet();
}

void Mod_MipiTxDscEnc_Config()
{
#if (MIPITX_VID_PATTERN_MODE != NO_PATTERN)
    Drv_MipiTxDscEnc_InitForPattern();
#else
    Drv_MipiTxDscEnc_Init();
#endif
    Mod_MipiTxDscEnc_PpsParaInit();
    Drv_MipiTxDscEnc_InfoGet();
    Mod_MipiTxDscEnc_ModuleEnable();
    Mod_MipiTxDscEnc_Clk_Chk();
}

